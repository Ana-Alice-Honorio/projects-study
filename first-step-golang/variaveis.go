package main

import "fmt"

func main() {

    // Strings, que podem ser adicionadas juntas com `+`.
    fmt.Println("go" + "lang")

    // Inteiros e floats.
    fmt.Println("1+1 =", 1+1)
    fmt.Println("7.0/3.0 =", 7.0/3.0)
    
    // Booleanos, com operadores booleanos como vocÃª esperaria.
    fmt.Println(true && false)
    fmt.Println(true || false)
    fmt.Println(!true)
}

